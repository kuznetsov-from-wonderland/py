from time import sleep

initial_color = 'r'
colors = {'r': 'üî¥ Red', 'g': 'üü¢ Green', 'y': 'üü° Yellow'}
seq = {'r': 'y', 'g': 'r', 'y': 'g'}
time_outs = {'r': 7, 'g': 9, 'y': 2}


class TrafficLight:
    color = initial_color

    def log(self):
        print(colors[self.color])

    def step(self):
        sleep(time_outs[self.color])
        next_color = seq[self.color]
        self.color = next_color

    def running(self):
        while True:
            self.log()
            self.step()


s = TrafficLight()
s.running()
